import * as React from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: "default" | "premium" | "glass" | "dark"
  }
>(({ className, variant = "default", ...props }, ref) => {
  const variants = {
    default: "rounded-xl border border-gray-200 bg-white shadow-sm",
    premium: "rounded-xl bg-white shadow-[0_4px_20px_-2px_rgba(0,0,0,0.08)] transition-all duration-300 hover:shadow-[0_20px_40px_-10px_rgba(22,163,74,0.35)] hover:-translate-y-1",
    glass: "rounded-xl bg-white/70 backdrop-blur-md border border-white/20 shadow-lg",
    dark: "rounded-xl bg-slate-900/80 backdrop-blur-md border border-white/10 shadow-lg text-white",
  }

  return (
    <div
      ref={ref}
      className={cn(variants[variant], className)}
      {...props}
    />
  )
})
Card.displayName = "Card"

